import java.math.*;
import java.util.*;
import java.io.*;

public class Main {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int c = sc.nextInt();
        int m = sc.nextInt();
        int k = sc.nextInt();

        if (c > m) {

            System.out.println(k*c);

        } else {

            System.out.println(k*m);

        }






    }
}